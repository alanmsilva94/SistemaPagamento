    /**
     * Ação ao perder o foco no campo de CNPJ - busca e preenche automaticamente
     * o nome da empresa
     */
    private void txtCnpjFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCnpjFocusLost
        String cnpj = txtCnpj.getText().trim();

        if (!cnpj.isEmpty()) {
            Empresa empresa = EmpresaDAO.BuscarPorCnpj(cnpj);
            if (empresa != null) {
                txtNomeEmpresa.setText(empresa.getNomeEmpresa());
            } else {
                JOptionPane.showMessageDialog(this, "Empresa não encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);
                txtNomeEmpresa.setText("");
            }
        }
    }//GEN-LAST:event_txtCnpjFocusLost

    /**
     * Ação ao perder o foco no campo de funcionário - busca e preenche
     * automaticamente e-mail e telefone
     */
    private void txtFuncionarioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtFuncionarioFocusLost
        String nomeFuncionario = txtFuncionario.getText().trim();

        if (!nomeFuncionario.isEmpty()) {
            Funcionario funcionario = FuncionarioDAO.buscarPorNome(nomeFuncionario);

            if (funcionario != null) {
                txtEmail.setText(funcionario.getEmail_fun());
                txtTelefone.setText(funcionario.getTelefone_fun());
            } else {
                JOptionPane.showMessageDialog(this, "Funcionário não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
                txtEmail.setText(""); // Limpa os campos se não encontrar
                txtTelefone.setText("");
            }
        }
    }//GEN-LAST:event_txtFuncionarioFocusLost

