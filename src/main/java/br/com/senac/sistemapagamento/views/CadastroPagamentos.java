    /**
     * Ação executada ao clicar no botão "Salvar"
     */
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Recupera a empresa pelo CNPJ
        String cnpj = txtCnpj.getText().trim();
        Empresa empresa = EmpresaDAO.BuscarPorCnpj(cnpj);
        if (empresa == null) {
            JOptionPane.showMessageDialog(this, "Empresa não encontrada!", "Erro", JOptionPane.ERROR_MESSAGE);
            return; // Interrompe o processo se a empresa não for encontrada
        }

        // Recupera o funcionário pelo nome
        String nome = txtFuncionario.getText().trim();
        Funcionario funcionario = FuncionarioDAO.buscarPorNome(nome);
        if (funcionario == null) {
            JOptionPane.showMessageDialog(this, "Funcionário não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
            return; // Interrompe o processo se o funcionário não for encontrado
        }

        // Formatar datas para o formato correto do MySQL (yyyy-MM-dd)
        SimpleDateFormat formatoEntrada = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat formatoMySQL = new SimpleDateFormat("yyyy-MM-dd");

        // Cria e preenche os dados do documento contábil
        DocumentoContabil doccontabil = new DocumentoContabil();
        doccontabil.setTipo_doc(cbxTipoDocumento.getSelectedItem().toString());
        doccontabil.setNum_doc(txtNumDocumento.getText());

        // Valida e formata a data de emissão do documento contábil
        try {
            Date dataEmissao = formatoEntrada.parse(txtDataEmissao.getText());
            doccontabil.setData_emissao(formatoMySQL.format(dataEmissao));
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Data de emissão inválida!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Define o valor e a descrição do documento contábil
        String valorDoc = txtValorDoc.getText();
        doccontabil.setValor(Double.parseDouble(valorDoc));
        doccontabil.setDescricao(txtDescricaoDoc.getText());

        // Salva o documento contábil no banco de dados
        DocumentoContabilDAO documentoContabilDAO = new DocumentoContabilDAO();
        documentoContabilDAO.cadastrar(doccontabil); // Salva o documento contábil

        // Cria e preenche os dados do pagamento
        Pagamento pagamento = new Pagamento();
        try {
            String dataTexto = txtDataPgto.getText().trim();
            if (dataTexto.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha a data de pagamento!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Date dataPgto = formatoEntrada.parse(dataTexto);
            pagamento.setData_pgto(formatoMySQL.format(dataPgto));
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Data de pagamento inválida! Use o formato dd/MM/yyyy", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Define os demais dados do pagamento
        String valorPgto = txtValorPgto.getText();
        pagamento.setValor_pgto(Double.parseDouble(valorPgto));
        pagamento.setBoleto(txtBoleto.getText());
        pagamento.setEmpresa(empresa);
        pagamento.setFuncionario(funcionario);

        // Salva o pagamento no banco de dados
        PagamentoDAO pagamentoDAO = new PagamentoDAO();
        pagamentoDAO.cadastrar(pagamento);

        // Atualiza a interface com os dados da empresa e exibe mensagem de sucesso
        TelaEmpresa telaEmpresa = new TelaEmpresa();
        telaEmpresa.exibirDados();
        JOptionPane.showMessageDialog(this, "Informações cadastradas!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        dispose(); // Fecha a janela após salvar os dados
    }//GEN-LAST:event_btnSalvarActionPerformed

