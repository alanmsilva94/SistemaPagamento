    // Exclui uma empresa e seus dados bancários selecionados
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tbEmpresa.getSelectedRow(); // Obtém a linha selecionada

        // Verifica se há uma linha selecionada
        if (linhaSelecionada != -1) { 
            Object valorEmpresaId = tbEmpresa.getValueAt(linhaSelecionada, 0);
            Object valorDadosBancariosId = tbEmpresa.getValueAt(linhaSelecionada, 0);

            if (valorEmpresaId != null && valorDadosBancariosId != null) {
                int empresaId = Integer.parseInt(valorEmpresaId.toString());
                int dadosBancariosId = Integer.parseInt(valorDadosBancariosId.toString());

                // Confirmação do usuário
                int confirmacao = JOptionPane.showConfirmDialog(
                        this, "Tem certeza que deseja excluir esta empresa e seus dados bancários?",
                        "Confirmação", JOptionPane.YES_NO_OPTION);

                if (confirmacao == JOptionPane.YES_OPTION) {
                    EmpresaDAO empresaDAO = new EmpresaDAO();
                    DadosBancarioDAO bancarioDAO = new DadosBancarioDAO();

                    // Excluir dados bancários pelo ID
                    bancarioDAO.excluirDadosBancarios(dadosBancariosId);

                    // Excluir a empresa pelo ID
                    empresaDAO.excluirEmpresa(empresaId);

                    // Atualizar a tabela após a exclusão
                    exibirDados();
                }
            } else {
                JOptionPane.showMessageDialog(this, "O valor selecionado é nulo.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma empresa para excluir.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

