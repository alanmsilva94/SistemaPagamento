    // Método responsável por preencher a tabela de empresas e seus dados bancários
    public void preencherTabela(List<Empresa> empresa) {
        String colunas[] = {
            "ID", "CNPJ", "Nome Empresa", "Nome Banco",
            "Código Banco", "Tipo Conta", "Agência",
            "Dígito Agência", "Conta", "Dígito Conta",
            "Chave Pix", "CNPJ Recebedor", "Nome Recebedor"
        };

        // Calcula o número total de linhas necessárias para exibir os dados bancários
        int totalLinhas = 0;
        for (Empresa e : empresa) {
            totalLinhas += e.getDadosBancarios().size();
        }

         // Matriz que armazenará os dados para serem exibidos na tabela
        String dados[][] = new String[totalLinhas][colunas.length];
        int i = 0;
        for (Empresa e : empresa) {
            for (DadosBancario db : e.getDadosBancarios()) {
                dados[i] = new String[]{
                    String.valueOf(e.getId()),
                    e.getCnpj(),
                    e.getNomeEmpresa(),
                    db.getNome_banco(),
                    String.valueOf(db.getCodigo_banco()),
                    db.getTipo_conta(),
                    String.valueOf(db.getAgencia()),
                    String.valueOf(db.getDigito_agencia()),
                    String.valueOf(db.getConta()),
                    String.valueOf(db.getDigito_conta()),
                    db.getChave_pix(),
                    db.getCnpj_recebedor(),
                    db.getNome_recebedor()
                };
                i++;
            }
        }

         // Preenche a tabela com os dados obtidos
        DefaultTableModel model = new DefaultTableModel(dados, colunas);
        tbEmpresa.setModel(model);
    }

     // Exibe os dados das empresas na tabela
    public void exibirDados() {
        try {
            EmpresaDAO empresaDAO = new EmpresaDAO();
            List<Empresa> resultados = empresaDAO.listar();
            preencherTabela(resultados);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados: " + ex.getMessage());
        }
    }

